// Relational Operator Example
public class RelationalOperator
{
    public static void main(String[] args) 
    {
        int x = 20, y = 20;
        // > Greater than
        System.out.println("x >  y = " + (x >  y) );
        // < Less than
        System.out.println("x <  y = " + (x <  y) );
        //== is equality to
        System.out.println("x == y = " + (x == y) );
        // != is not equal to
        System.out.println("x != y = " + (x != y) );
        // >= Greater than or equal to
        System.out.println("x >= y = " + (x >= y) );
        // <= Less than or equal to
        System.out.println("x <= y = " + (x <= y) );
    }
}